#!/usr/bin/env node
/**
 * Point local .env.local at the isolated demo Neon cell + cargo-logistics profile.
 * Use for building/testing against demo data without touching app.getstride.co.ke DB.
 *
 * Usage: npm run demo:cell:local
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const cellPath = path.join(root, '.env.demo-cell.local');
const localPath = path.join(root, '.env.local');
const profilePath = path.join(root, 'deployments', 'demo-getstride.env');

function parseEnv(text) {
  const map = new Map();
  for (const line of text.split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq < 0) continue;
    let v = t.slice(eq + 1).trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
      v = v.slice(1, -1);
    }
    map.set(t.slice(0, eq).trim(), v);
  }
  return map;
}

if (!existsSync(cellPath)) {
  console.error('Missing .env.demo-cell.local — run demo cell Neon setup first.');
  process.exit(1);
}

const cell = parseEnv(readFileSync(cellPath, 'utf8'));
const profile = parseEnv(readFileSync(profilePath, 'utf8'));
const local = existsSync(localPath) ? parseEnv(readFileSync(localPath, 'utf8')) : new Map();

local.set('DATABASE_URL', cell.get('DATABASE_URL') || '');
local.set('DIRECT_DATABASE_URL', cell.get('DIRECT_DATABASE_URL') || '');
local.set('NEXT_PUBLIC_SITE_URL', 'http://localhost:3000');
local.set('NEXTAUTH_URL', 'http://localhost:3000');

for (const [k, v] of profile) {
  if (k === 'NEXT_PUBLIC_SITE_URL' || k === 'NEXTAUTH_URL') continue;
  local.set(k, v);
}

const lines = [
  '# Generated by npm run demo:cell:local — demo Neon + SwiftFreight profile',
  ...[...local.entries()].map(([k, v]) => `${k}=${JSON.stringify(v).slice(1, -1)}`),
];
writeFileSync(localPath, `${lines.join('\n')}\n`, 'utf8');
console.log('✓ .env.local → demo cell DB + cargo-logistics profile');
console.log('  Run: npm run dev');
console.log('  Restore production local env from backup if needed.');
